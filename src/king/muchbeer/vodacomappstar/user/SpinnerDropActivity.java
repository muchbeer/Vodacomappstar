package king.muchbeer.vodacomappstar.user;


public class SpinnerDropActivity {
	 private String title;
	    private int icon;
	     
	    public SpinnerDropActivity(String title, int icon){
	        this.title = title;
	        this.icon = icon;
	    }
	     
	    public String getTitle(){
	        return this.title;      
	    }
	     
	    public int getIcon(){
	        return this.icon;
	    }

}
